public class Variables {
  public static void main(String[] args) {
	  
	String name = "Mohamed Sabra";
	System.out.println(name);  
	
	name = "Mohamed N. Sabra (new value)";
	System.out.println(name);  
	
	int occupation_code = 2173;
	System.out.println(occupation_code);
	
	occupation_code = 2174;
	System.out.println(occupation_code);
	
	int age; //assign a value later
	age = 30;
	System.out.println(age);
	
  }
}