public class PlusOperator {
  public static void main(String[] args) {
	  
	// + operator used for concatenation when variables are of type String  
    String firstName = "Mohamed ";
	String lastName = "Sabra";
	String fullName = firstName + lastName;
	System.out.println(fullName);
	
	// + operator used for concatenation when variables are of type String 
	int a = 10;
	int b = 23;
	System.out.println(a + b); // Print the value of the result of a + b as a mathematical operation
		

  }
}